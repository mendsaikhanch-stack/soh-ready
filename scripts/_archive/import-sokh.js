(async () => {
  const { data: districts } = await s.from('districts').select('*');
  const { data: khoroos } = await s.from('khoroos').select('*');

  // Find and create missing khoroos
  const missingSet = new Set();
  for (const [abbr, num] of data) {
    const dName = DISTRICT_MAP[abbr];
    const dist = districts.find(d => d.name === dName);
    if (!dist) { console.log('District not found:', abbr, dName); continue; }
    if (!khoroos.find(k => k.district_id === dist.id && k.name === num + '-р хороо')) {
      missingSet.add(dist.id + ':' + num);
    }
  }

  if (missingSet.size > 0) {
    const toInsert = [...missingSet].map(k => {
      const [d, n] = k.split(':');
      return { district_id: +d, name: n + '-р хороо' };
    });
    console.log('Дутуу хороо нэмж байна:', toInsert.map(k => k.name + ' (district_id=' + k.district_id + ')').join(', '));
    const { error } = await s.from('khoroos').insert(toInsert);
    if (error) { console.log('Khoroo insert error:', error.message); return; }
  }

  // Reload khoroos
  const { data: allK } = await s.from('khoroos').select('*');

  // Insert organizations
  let ok = 0, fail = 0;
  for (const [abbr, num, name, contact, phone] of data) {
    const dName = DISTRICT_MAP[abbr];
    const dist = districts.find(d => d.name === dName);
    const khoroo = allK.find(k => k.district_id === dist.id && k.name === num + '-р хороо');
    if (!khoroo) { console.log('Khoroo not found:', dName, num); fail++; continue; }

    const { error } = await s.from('sokh_organizations').insert({
      name,
      khoroo_id: khoroo.id,
      phone,
      address: dName + ' дүүрэг, ' + num + '-р хороо',
    });
    if (error) { console.log('ERR:', name, error.message); fail++; }
    else ok++;
  }
  console.log('\n' + ok + ' СӨХ нэмэгдлээ, ' + fail + ' алдаа');

  // Summary by district/khoroo
  const { data: all } = await s.from('sokh_organizations').select('*, khoroos(name, districts(name))').order('id');
  const g = {};
  for (const o of all) {
    const key = (o.khoroos?.districts?.name || '?') + ' / ' + (o.khoroos?.name || '?');
    if (!g[key]) g[key] = [];
    g[key].push(o.name);
  }
  console.log('\nХарьяалалаар:');
  for (const [loc, orgs] of Object.entries(g)) {
    console.log('  ' + loc + ' (' + orgs.length + '): ' + orgs.join(', '));
  }
})();
